<section class="whoweare">
	<h3>
		THE BLACK STAR INTERNATIONAL FILM FESTIVAL (BSIFF) IS A MOVEMENT OF CHANGE MAKERS INSPIRING THE
		BUSINESS OF FILM
	</h3>
    <p>
        Like the Black Star, BSIFF provides a platform that inspires hope & love for film, cinema, and film-music, supporting independent African filmmakers to reach the right network, worldwide audience, film education, acquire relevant skills and distribution opportunities that push their creative boundaries beyond limit.

    </p>
    <p>
        BSIFF aspires to bridge the gap between Africa Cinema and the world. To achieve this, we are committed to: 
    </p>
    <ul>
        <li>
            <p>
               - discovering, displaying, and celebrating the works of independent film artists in Ghana and Africa to audiences (existing and new) around the World, through our film festival.
            </p>
        </li>
        <li>
            <p>
               - educating, nurturing, and providing skill training & film distribution opportunities to independent emerging and aspiring film artists in Ghana and Africa, through our educational platform, BSIFF Edu.
            </p>
        </li>
        <li>
            <p>
               - building a community of film enthusiasts and providing a platform where they can connect, network, foster the exchange of cultures and explore business opportunities and investments.
            </p>
        </li>
    </ul>
</section>
